import turtle

win = turtle.Screen()
win.bgcolor("black")
win.title("I got this!")

Taylor = turtle.Turtle()
Taylor.pensize(4)
Taylor.pencolor("purple")
Taylor.shape("turtle")

Taylor.forward(50)

Taylor.penup()
Taylor.right(200)
Taylor.pendown()

Taylor.left(100)
Taylor.forward(90)




win.mainloop()

 
